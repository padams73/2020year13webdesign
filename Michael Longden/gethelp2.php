
<p>To access the HELP you need from this website, you first need to select
your NCEA level and then this will bring up all the maths standards that
HELP is available. Then select the standard you need help in and this
will bring up all the different HELP available for that standard. Select an
activity and this will bring you to the activity page. If you leave an activity
in the middle of completing it, it will show under the Recently Viewed on
your personal home page. Just select that activity and it will immediately
bring you back to that activity. Whenever you want to go back to your
personal home page, click on the HELP logo.</p><!--This paragraph appears as text on the website.-->
